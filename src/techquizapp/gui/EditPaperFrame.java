    private void jcSubjectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcSubjectActionPerformed
        if(jcSubject.getSelectedIndex()==0)
        {
            JOptionPane.showMessageDialog(null, "Please Select a Subject","Error",JOptionPane.ERROR_MESSAGE);
            jcExamId.removeAllItems();
            return;
        }
//        System.out.println("Button Clicked");
        String subject=jcSubject.getSelectedItem().toString();
        try
        {
            ArrayList<String> quesList=new ArrayList<>();
            quesList=ExamDAO.getExamIdBySubject(subject);
            if(quesList.isEmpty())
            {
                JOptionPane.showConfirmDialog(null, "No paper found in the Database","Edit Paper Error",JOptionPane.ERROR_MESSAGE);
                return;
            }
            jcExamId.removeAllItems();
            for(String e:quesList)
            {
                jcExamId.addItem(e);
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showConfirmDialog(null, "DB Error","Edit Paper Error",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jcSubjectActionPerformed

    private void btnEditQuestionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditQuestionActionPerformed
        if(jcSubject.getSelectedIndex()==0)
        {
            JOptionPane.showMessageDialog(null, "Please Select a Subject","Error",JOptionPane.ERROR_MESSAGE);
            jcExamId.removeAllItems();
            return;
        }
        String examId=jcExamId.getSelectedItem().toString();
        String subject=jcSubject.getSelectedItem().toString();
        int ans=JOptionPane.showConfirmDialog(null, "You want to change the "+subject+" Paper "+examId+".\nAre you sure?","Confirm!",JOptionPane.YES_NO_OPTION,JOptionPane.INFORMATION_MESSAGE);
        if(ans==JOptionPane.YES_OPTION)
        {
            try
            {
                int totalQues=ExamDAO.getQuestionCountByExam(examId);
                Exam editExam=new Exam(examId,subject,totalQues);
                EditQuestionsFrame editQuestion=new EditQuestionsFrame(editExam);
                editQuestion.setVisible(true);
                this.dispose();
            }
            catch(SQLException ex)
            {
                JOptionPane.showMessageDialog(null, "DB Error","Edit Paper Error",JOptionPane.ERROR_MESSAGE);
            }
        }
           
        
        
    }//GEN-LAST:event_btnEditQuestionActionPerformed

