    private void loadSubjects()
    {
        try
        {
            ArrayList<String> subjectList=new ArrayList<>();
            subjectList=ExamDAO.getAllSubjects();
            if(subjectList.isEmpty())
            {
                JOptionPane.showMessageDialog(null, "No Papers found in the Database","Not Found",JOptionPane.INFORMATION_MESSAGE);
            }
            for(String e:subjectList)
            {
                jcSubject.addItem(e);
            }
            
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "DB Error","Edit Paper Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }

