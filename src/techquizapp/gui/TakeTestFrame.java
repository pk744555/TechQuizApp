    private void btnDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoneActionPerformed
        c.stop();
        String chosenAnswer=getuserAnswer();
       if(chosenAnswer!=null)
       {
           Question ques=qStore.getQuestionByQno(qno);
           String correctAnswer=ques.getCorrectAnswer();
           Answer ans=new Answer(examobj.getExamId(),examobj.getLanguage(),qno,chosenAnswer,correctAnswer);
           Answer attempted=aStore.getAnswerByQno(qno);
           if(attempted==null)
           {
               aStore.addAnswer(ans);
           }
           else
           {
               String previous=attempted.getChosenAnswer();
               if(chosenAnswer.equals(previous)==false)
               {
                   int apos=aStore.removeAnswer(attempted);
                   aStore.setAnswerAt(apos, ans);
               }
           }
           
       }
       int right=0, wrong=0;
       for(Question ques:qStore.getAllQuestions())
       {
           int qno=ques.getQno();
           Answer ans=aStore.getAnswerByQno(qno);
           if(ans==null)
               continue;
           String attemptedAnswer=ans.getChosenAnswer();
           String correctAnswer=ans.getCorrectAnswer();
           if(attemptedAnswer.equals(correctAnswer))
               ++right;
           else
               ++wrong;
           
       }
        StringBuilder sb=new StringBuilder();
        sb.append("\nRight answers : "+right);
        sb.append("\nWrong answers: "+wrong);
        sb.append("\nUnattempted: "+(qStore.getCount()-(right+wrong)));
        JOptionPane.showMessageDialog(null, sb.toString(),"Result",JOptionPane.INFORMATION_MESSAGE);
        try
        {
            Performance perf=new Performance(UserProfile.getUsername(),examobj.getExamId(),right,wrong,(qStore.getCount()-(right+wrong)),(double)right/qStore.getCount()*100,examobj.getLanguage());
            PerformanceDAO.addPerformance(perf);
            JOptionPane.showMessageDialog(null, "Result Saved\nThankyou for the test","Done",JOptionPane.INFORMATION_MESSAGE);
            PaperSelectionFrame paperFrame=new PaperSelectionFrame();
            paperFrame.setVisible(true);
            this.dispose();
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "DB Error","TakeTest Error",JOptionPane.ERROR_MESSAGE);
        }
       
       
       
    }//GEN-LAST:event_btnDoneActionPerformed

