        class Clocks extends Thread{
        int time=0;
          int m=1,s=60,h=0;
       public void run(){
       while(true){
               Calendar date=Calendar.getInstance();
               date.setTime(new Date());
               date.set(Calendar.HOUR_OF_DAY, h);
               date.set(Calendar.MINUTE, m);
               date.set(Calendar.SECOND, s);
               date.set(Calendar.MILLISECOND, 0);
               Date da=date.getTime();
               SimpleDateFormat sdf=new SimpleDateFormat("HH:mm:ss");
               String str=sdf.format(da);
               
        try{
            Thread.sleep(1000);
            s=s-1;
            if(s==0){
                m=m-1;
                s=60;
                time=time+1;
            }
            
            //time=time+1000;
            lblClock.setText(str);
            }
            catch(InterruptedException ex){
                System.out.println(ex);
            }
           if(time==2){
               lblClock.setText("00:00:00");
               JOptionPane.showMessageDialog(null, " TIME IS FINISHED PRESS DONE TO SUBMIT EXAM","TIME OUT",JOptionPane.INFORMATION_MESSAGE);
               //doneTest();
               btnNext.setEnabled(false);
               btnPrevious.setEnabled(false);
               btnCancel.setVisible(false);
               jlblLogout.setVisible(false);
               break;
           }
       }
         }
    }
